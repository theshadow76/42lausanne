#include <unistd.h>

void	ft_print_comb(void)
{
	char	i;
	char	k;
	char	j;

	i = '0';
	while (i <= '7')
	{
		k = i + 1;
		while (k <= '8')
		{
			j = k + 1;
			while (j <= '9')
			{
				write(1, (char []){i, k, j}, 3);
				if (!(i == '7' && k == '8' && j == '9'))
					write(1, ", ", 2);
				j++;
			}
			k++;
		}
		i++;
	}
}
