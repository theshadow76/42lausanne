#include <unistd.h>

void	ft_print_comb2(void)
{
	char	tab[4];
	int		a;
	int		b;

	a = 0;
	b = 0;
	while (a <= 99)
	{
		b = a + 1;
		while (b <= 99)
		{
			tab[0] = (a / 10) + '0';
			tab[1] = (a % 10) + '0';
			tab[2] = (b / 10) + '0';
			tab[3] = (b % 10) + '0';
			write(1, (char []){tab[0], tab[1], ' ', tab[2], tab[3]}, 5);
			if (!(a == 98 && b == 99))
				write(1, (char []){',', ' '}, 2);
			b++;
		}
		a++;
	}
}
